class Solution:
    def longestWord(self, words):
        if not words:
            return ''
        words.sort()
        result = ''
        rm = {}
        last = None
        for i in range(len(words)-1, -1, -1):
            word = words[i]

            # 被迫去重
            if word == last:
                continue
            else:
                last = word

            if word not in rm:
                if len(word) < len(result):
                    continue
                rm[word] = word

            r = rm.pop(word)
            if len(r) < len(result):
                continue

            if len(word) == 1:
                result = r
            else:
                prev = word[:-1]
                # 有一种情况是后来较短的新加入元素反倒覆盖了早先加入的长元素
                if prev in rm and len(rm[prev]) > len(r):
                    continue
                rm[prev] = r

        return result


